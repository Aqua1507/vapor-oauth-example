import Leaf
import Vapor

public func configure(_ app: Application) throws {

   //      =============================================================
   //      Debugging
   //      =============================================================

   app.logger.logLevel = .notice

   //      =============================================================
   //      Port
   //      =============================================================

   app.http.server.configuration.port = 8089

   //      =============================================================
   //      Renderer
   //      =============================================================

    app.views.use(.leaf)

   //      =============================================================
   //      Routes
   //      =============================================================

   let routers: [RouteCollection] = [Router()]

   for router in routers {
      try router.boot(routes: app.routes)
   }

}
